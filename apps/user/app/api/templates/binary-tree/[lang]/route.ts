export async function GET(
  _: Request,
  { params }: { params: { lang: string } }
): Promise<NextResponse<{ code: string }>> {
  const lang = params.lang;
  if (!(lang in data)) {
    return NextResponse.json({ code: "" }, { status: 400 });
  }
  return NextResponse.json({ code: data[lang as Language] });
}
